def start_adventure():
    print("Вы оказались на распутье в темном лесу.")
    print("Слева вы видите таинственную тропинку, а справа — старый мост.")
    choice1 = input("Куда вы пойдете? Введите 'тропинка' или 'мост': ").strip().lower()

    if choice1 == 'тропинка':
        print("\nВы пошли по тропинке и вскоре наткнулись на странный дом.")
        choice2 = input("Вы хотите зайти в дом? Введите 'да' или 'нет': ").strip().lower()
        if choice2 == 'да':
            print("\nВы зашли в дом и нашли сундук с сокровищами. Вы победили!")
        elif choice2 == 'нет':
            print(
                "\nВы решили обойти дом стороной и продолжили свой путь. Вскоре вы нашли дорогу домой. Вы в безопасности!")
        else:
            print("\nВаш выбор неясен. Вы потерялись в лесу.")

    elif choice1 == 'мост':
        print("\nВы направились к мосту, но он оказался слишком старым и ненадежным.")
        choice3 = input("Вы хотите рискнуть и перейти мост? Введите 'да' или 'нет': ").strip().lower()
        if choice3 == 'да':
            print("\nК сожалению, мост рухнул, и вы упали в реку. Вы проиграли.")
        elif choice3 == 'нет':
            print("\nВы решили вернуться назад и нашли другую дорогу через лес. Вы в безопасности!")
        else:
            print("\nВаш выбор неясен. Вы потерялись в лесу.")

    else:
        print("\nВаш выбор неясен. Вы потерялись в лесу.")


# Запуск игры
start_adventure()